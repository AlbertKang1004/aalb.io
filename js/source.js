function zoomAnimation () {
  document.querySelector(".title-image").style.backgroundSize = "103%";
  document.querySelector(".title-image").style.transition = "0.4s";
}

function zoomAnimationReverse () {
  document.querySelector(".title-image").style.backgroundSize = "100%";
  document.querySelector(".title-image").style.transition = "0.4s";
}

